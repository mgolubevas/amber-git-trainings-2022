# Simple calculator Implementation in Python


def add(arg1, arg2):
  result = arg1 + arg2
  return(result)

def subtract(arg1, arg2):
  result = arg1 - arg2
  return(result)

def multiply(arg1, arg2):
  result = arg1 * arg2
  return(result)

def divide(arg1, arg2):
  result = arg1 / arg2
  return(result)